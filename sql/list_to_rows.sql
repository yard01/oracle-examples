select cast (dt as date) from (
SELECT REGEXP_SUBSTR('DATE''2016-04-01'',DATE''2016-01-01'',DATE''2017-01-01'',DATE''2016-10-01'',DATE''2016-07-01''','[^,]+', 1, LEVEL) dt FROM DUAL 
CONNECT BY REGEXP_SUBSTR('DATE''2016-04-01'',DATE''2016-01-01'',DATE''2017-01-01'',DATE''2016-10-01'',DATE''2016-07-01''','[^,]+', 1, LEVEL) IS NOT NULL);
